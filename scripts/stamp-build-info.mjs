import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, '..');
const packageJsonPath = path.join(repoRoot, 'package.json');
const outPath = path.join(repoRoot, 'src', 'config', 'buildInfo.js');

const pkgRaw = await fs.readFile(packageJsonPath, 'utf8');
const pkg = JSON.parse(pkgRaw);

const buildNumber = process.env.GITHUB_RUN_NUMBER || 'dev';
const baseVersion = pkg.version || '0.0.0';

// Minimal: version counter is the workflow run number.
const buildVersion = buildNumber === 'dev' ? `${baseVersion}+dev` : `${baseVersion}+${buildNumber}`;

const content = `// AUTO-GENERATED by scripts/stamp-build-info.mjs\n// Do not edit by hand; CI/CD overwrites this at deploy-time.\n\nexport const BUILD_NUMBER = ${JSON.stringify(buildNumber)};\nexport const BUILD_VERSION = ${JSON.stringify(buildVersion)};\n`;

await fs.mkdir(path.dirname(outPath), { recursive: true });
await fs.writeFile(outPath, content, 'utf8');

console.log(`Stamped build info -> ${path.relative(repoRoot, outPath)} (${buildVersion})`);
